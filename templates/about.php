<div style="margin:20px;">
    <h1>Welcome to the BMI Chart</h1>
    <p>
        This chart shows your
        <a href="#bmi">BMI (Body Mass Index)</a> and
        <a href="#bmi-prime">BMI Prime</a> for a range of weights
        based on your height, age, and sex.
    </p>
    <p>
        Your BMI is used to estimate the average body composition
        (<a href="#body-composition">Body Fat and Lean Mass</a> percentages)
    </p>
    <p>
        The body composition is used to estimate caloric maintenance requirements.
        First we estimate the
        <a href="#bmr">BMR (Base Metabolic Rate)</a>,
        and then
        <a href="#tdee">TDEE (Total Daily Energy Expenditure)</a> for various activity levels.
    </p>
    <p>
        The BMI Chart is an Open Source project.  Learn more at:
        <a href="https://github.com/attogram/bmi-chart" target="github">https://github.com/attogram/bmi-chart</a>
    </p>

    <a name="howto"></a>
    <h2>How to use the chart</h2>
    <p>
        Enter your height (in meters), your age, and your sex.  Then click 'Update Chart'.
    </p>
    <p>
        In the Chart options section you can customize the weight ranges used in the chart,
        and the increment between weights.
    </p>
